export function getFingerForKey(char: string): FingerInfo {
  return FINGER_MAP[char.toLowerCase()] || FINGER_MAP[char] || { finger: 'Unknown', colorClass: '' };
}

export function getKeyboardKeyForChar(char: string): string {
  if (char === '\n') return 'Enter';
  if (char === '\t') return 'Tab';
  if (char === ' ') return ' ';
  const shiftMap: Record<string, string> = {
    '~': '`', '!': '1', '@': '2', '#': '3', '$': '4', '%': '5',
    '^': '6', '&': '7', '*': '8', '(': '9', ')': '0', '_': '-',
    '+': '=', '{': '[', '}': ']', '|': '\\', ':': ';', '"': "'",
    '<': ',', '>': '.', '?': '/',
  };
  return shiftMap[char] || char.toLowerCase();
}
